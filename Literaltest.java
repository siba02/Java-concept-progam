public class Literaltest {
    public static void main(String[] args){
        int num1 = 10;
        int num2 = 012;
        int num3 = 0x123;
        int num4 = 0b1010;
        
        System.out.println("Integer 10 in decimal: " + num1);
        System.out.println("Integer 012 in octal: " + num2);
        System.out.println("Integer 0x1A in hexadecimal: " + num3);
        System.out.println("Integer 0b1010 in binary: " + num4);
    }
    
}
